package array;

public class Variable {

    public static void main(String[] args) {
        int ballpen = 2000;
        int eraser = 1000;
        int mechanical_pencil = 3000;
        int marker = 3000;

        int sum = ballpen + eraser + mechanical_pencil + marker;

        System.out.println("ballpen = " + ballpen);
        System.out.println("eraser = " + eraser);
        System.out.println("mechanical_pencil = " + mechanical_pencil);
        System.out.println("marker = " + marker);

        System.out.println("합계 = " + sum);
    }
}
